#Math function
import math
print(math.sqrt(4),"sqrt")
print(math.ceil(1.55555),"ceil")
print(math.floor(1.55555),"floor")
print(math.factorial(5),'fact')
print(math.fabs(-5),"fabs")
print(math.pow(2,3),"'2 to the power of 3'")
print(math.log2(2),"log2")
print(math.pi)
print(math.e)
